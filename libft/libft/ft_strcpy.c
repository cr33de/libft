#include "libft.h"

char		*ft_strcpy(char *dst, const char *src)
{
	int		i;
	size_t	len;

	if (dst == NULL || src == NULL)
		len = ft_strlen(src);
	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
